    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $deposit = new Deposit();
        $deposit->renter = $request->name;
        $deposit->renters_id = $request->renters_id;
        $deposit->flats_id = $request->flats_id;
        $deposit->total = $request->rent;
        $deposit->pay_by = $request->pay_by;
        $deposit->buildings_id = $request->buildings_id;

        if($deposit->save()){
            return new DepositResource($deposit);        }
    }

